// Simple Icons wrapper for brand icons
export const SimpleIcon = ({ 
  icon, 
  color = 'FFFFFF', 
  className = "w-5 h-5",
  size = 20 
}: { 
  icon: string; 
  color?: string; 
  className?: string;
  size?: number;
}) => {
  const iconName = icon.toLowerCase().replace(/\s+/g, '');
  // Handle color - if it's a CSS color name, convert to hex, or use as-is
  const iconColor = color.replace('#', '');
  return (
    <img 
      src={`https://cdn.simpleicons.org/${iconName}/${iconColor}`}
      alt={icon}
      className={className}
      style={{ width: size, height: size }}
    />
  );
};

